import { Router } from "../config/deps.ts";
import { UserController } from "../controllers/user.ts";
import { UserModel } from "../models/users.ts";
import { validationMiddleware } from "../middlewares/validation.ts";
const router = new Router();
const user = new UserController();


/**
 * @swagger
 * definitions:
 *   User:
 *     properties:
 *       first_name:
 *         type: string
 *       last_name:
 *         type: string
 *       email:
 *         type: string
 */

 /**
 * @swagger
 * /createuser:
 *   post:
 *     tags:
 *       - Users
 *     description: Creates a new user
 *     produces:
 *       - application/json
 *     parameters:
 *       - name: user
 *         description: User object
 *         in: body
 *         required: true
 *         schema:
 *           $ref: '#/definitions/User'
 *     responses:
 *       200:
 *         description: Successfully created
 */

router.post("/createuser", validationMiddleware(UserModel), user.createUser);


/**
 * @swagger
 * /getuser/{id}:
 *   get:
 *     tags:
 *       - Users
 *     description: Returns a single user
 *     produces:
 *       - application/json
 *     parameters:
 *       - name: id
 *         description: User's id
 *         in: path
 *         required: true
 *         type: string
 *     responses:
 *       200:
 *         description: A single user
 *         schema:
 *           $ref: '#/definitions/User'
 */

router.get("/getuser/:id", user.getUserById);

/**
 * @swagger
 * /updateuser/{id}:
 *   put:
 *     tags:
 *       - Users
 *     description: Updates a single user
 *     produces:
 *       - application/json
 *     parameters:
 *       - name: id
 *         description: User's id
 *         in: path
 *         required: true
 *         type: string
 *       - name: user
 *         description: User object
 *         in: body
 *         required: true
 *         schema:
 *           $ref: '#/definitions/User'
 *     responses:
 *       200:
 *         description: Successfully updated
 */

router.put("/updateuser/:id", validationMiddleware(UserModel), user.updateUserById);

/**
 * @swagger
 * /deleteuser/{id}:
 *   delete:
 *     tags:
 *       - Users
 *     description: Deletes a single user
 *     produces:
 *       - application/json
 *     parameters:
 *       - name: id
 *         description: User's id
 *         in: path
 *         required: true
 *         type: string
 *     responses:
 *       200:
 *         description: Successfully deleted
 */
router.delete("/deleteuser/:id", user.deleteUserById);


router.get("/book/:id", user.getBookByID);

export { router };
